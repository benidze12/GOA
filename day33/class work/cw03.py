#ინდექსი აღემატება სიის რაოდენობას დააბრუნეთ ერორი სტრინგის სახით სხვაშემთხვევაში დაამატეთ სიაში

names = ["გიორგი", "ანა", "ლიკა", "დათა", "ნინო"]
name = input("შეიყვანეთ სახელი: ")
index = int(input("შეიყვანეთ ინდექსი: "))
if index > len(names):
    print("რაღაცა.")
else:
    names.insert(index, name)
    print("სია:", names)
