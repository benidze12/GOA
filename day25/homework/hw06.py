#­ЪЊЮ рЃЊрЃћрЃбрЃљрЃџрЃћрЃЉрЃў:
#рЃбрЃћрЃЦрЃАрЃбрЃў рЃБрЃюрЃЊрЃљ рЃњрЃљрЃўрЃДрЃЮрЃА рЃАрЃўрЃбрЃДрЃЋрЃћрЃЉрЃљрЃЊ.
#рЃЌрЃўрЃЌрЃЮрЃћрЃБрЃџ рЃАрЃўрЃбрЃДрЃЋрЃљрЃА рЃБрЃюрЃЊрЃљ рЃЊрЃљрЃћрЃЏрЃљрЃбрЃЮрЃА рЃЏрЃўрЃАрЃў рЃАрЃўрЃњрЃарЃФрЃћ.
#(рЃљрЃа рЃљрЃарЃўрЃА рЃљрЃБрЃфрЃўрЃџрЃћрЃЉрЃћрЃџрЃў рЃљрЃЏ рЃЊрЃљрЃЋрЃљрЃџрЃћрЃЉрЃўрЃА рЃерЃћрЃАрЃарЃБрЃџрЃћрЃЉрЃљ рЃЌрЃБ рЃерЃћрЃФрЃџрЃћрЃЉрЃЌ рЃњрЃљрЃљрЃЎрЃћрЃЌрЃћрЃЌ)

def word_lengths(text):
    words = text.split()
    return [f"{word} {len(word)}" for word in words]
print(word_lengths("hello world python"))
