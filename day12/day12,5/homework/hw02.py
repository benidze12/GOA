#2) ჩამოწერეთ 5-5 მაგალითი შემდეგ ოპერატორებზე:  + , - , * , / , ** , %

print(2 + 3)   # 5
print(10 + 5)  # 15
print(-1 + 1)  # 0
print(7 + 8)   # 15
print(0 + 5)   # 5

print(5 - 3)   # 2
print(10 - 4)  # 6
print(2 - 5)   # -3
print(8 - 2)   # 6
print(11 - 11) # 0

print(2 * 3)   # 6
print(5 * 4)   # 20
print(-1 * 2)  # -2
print(7 * 3)   # 21
print(0 * 5)   # 0

print(6 / 3)   # 2.0
print(10 / 2)  # 5.0
print(5 / -1)  # -5.0
print(8 / 2)   # 4.0
print(0 / 5)   # 0.0

print(2 ** 3)  # 8
print(5 ** 2)  # 25
print(3 ** 4)  # 81
print(10 ** 0) # 1
print(-2 ** 2) # 4

print(5 % 2)   # 1
print(10 % 3)  # 1
print(8 % 4)   # 0
print(9 % 5)   # 4
print(15 % 4)  # 3