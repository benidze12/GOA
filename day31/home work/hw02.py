# სტრინგის (string) ფუნქციები:

# .lower() - გადააქცევს სტრინგს პატარა ასოებად
# .upper() - გადააქცევს სტრინგს დიდ ასოებად
# .capitalize() - გადააქცევს მხოლოდ პირველ ასოს დიდად
# .strip() - აშორებს სიცარიელეებს სტრინგის თავიდან და ბოლოდან
# .replace(old, new) - ცვლის ტექსტში მონაკვეთებს სხვა მნიშვნელობით
# .find(substring) - აბრუნებს ინდექსს სადაც substring პირველად გვხვდება
# .startswith(prefix) - ამოწმებს, იწყება თუ არა სტრინგი მოცემული მნიშვნელობით
# .endswith(suffix) - ამოწმებს, მთავრდება თუ არა სტრინგი მოცემული მნიშვნელობით
# .split(separator) - ყოფს სტრინგს სიად მოცემული გამყოფით
# .join(list) - აერთიანებს ელემენტებს სტრინგად კონკრეტული გამყოფით

